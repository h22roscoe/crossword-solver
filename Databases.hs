sortedWordlist
  = trace "Sorted word map..." (Map.fromList (foldl' f [("",[])] ps))
  where
    ps = sortBy (\x y -> compare (fst x) (fst y))
                (Prelude.map makeEl (Set.elems allWords))
    makeEl w = (sort w, w)

f acc@((cs', ws) : m) (cs, w)
  | cs == cs' = (cs', w : ws) : m
  | otherwise = (cs, [w]) : acc

